package chaptertwo.partthree;

class TwoThreeFour {
    public static void main (String[] args) {
        int x = 100;
        int z = 90;
        String y = "Значение x равно ";
        System.out.println(x > z);
        System.out.println(x < z);
        System.out.println(x);
        System.out.println(x + z);
        System.out.println(x * z);
        System.out.println(x / z);
        System.out.println("x = " + x);
        System.out.println(y + x);
        System.out.println(y + x + z);
        System.out.println(y + (x + z));
        System.out.println(x + " " + z);
    }
}