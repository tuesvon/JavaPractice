package chaptertwo.partfive;

import java.util.Scanner;

class TwoFiveNine {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int x = a * a;
        int y = a * a * a;
        System.out.println(a + " " + x + " " + y);
    }
}