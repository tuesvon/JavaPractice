import java.util.Scanner;

class TwoFiveEleven {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        double a = sc.nextDouble();
        double b = sc.nextDouble();
        double c = sc.nextDouble();
        double z = (a + b + c) / 3;
        System.out.println(z);
    }
}